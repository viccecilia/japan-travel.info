function toHant(value) {
  let text = String(value ?? "");
  for (const [from, to] of hantPairs) text = text.replaceAll(from, to);
  return text;
}
function walkHant(value) {
  if (Array.isArray(value)) return value.map(walkHant);
  if (value && typeof value === "object") return Object.fromEntries(Object.entries(value).map(([k, v]) => [k, walkHant(v)]));
  return typeof value === "string" ? toHant(value) : value;
}
function cleanCjk(value, replacement) {
  if (Array.isArray(value)) return value.map((item) => cleanCjk(item, replacement));
  if (value && typeof value === "object") return Object.fromEntries(Object.entries(value).map(([k, v]) => [k, cleanCjk(v, replacement)]));
  if (typeof value !== "string") return value;
  return value
    .replace(/[\u3400-\u9fff]+/g, replacement)
    .replace(/\s+/g, " ")
    .trim();
}

